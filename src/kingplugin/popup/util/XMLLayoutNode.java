package kingplugin.popup.util;

public class XMLLayoutNode {
	private String type;
	private String id;

	public XMLLayoutNode(String type, String id) {
		super();
		this.type = type;
		this.id = id;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
}
